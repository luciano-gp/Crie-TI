    ArrayList<Cliente> clientes = new ArrayList();

    private void btnCadastrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCadastrarMouseClicked

        if (clientes.size() >= 4) {
            Mensagem.erro("Espa√ßo insuficiente para cadastrar mais clientes");
        } else {
            Cliente objCliente = new Cliente();

            objCliente.setNome(txtNome.getText());
            objCliente.setSexo(cmbSexo.getItemAt(cmbSexo.getSelectedIndex()));
            objCliente.setIdade(Integer.parseInt(txtIdade.getText()));
            objCliente.setNatural(txtNatural.getText());
            objCliente.setAltura(Double.parseDouble(txtAltura.getText()));
            objCliente.setPeso(Double.parseDouble(txtPeso.getText()));

            clientes.add(objCliente);
            limparCampos();
        }
    }//GEN-LAST:event_btnCadastrarMouseClicked

    private void btnMostrarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnMostrarMouseClicked
        
        StringBuilder todosClientes = new StringBuilder();
        todosClientes.append("Clientes cadastrados:\n");
        
        for (int i = 0; i < clientes.size(); i++) {
            todosClientes.append(clientes.get(i).toString());
        }
        
        txtMostrarClientes.setText(todosClientes.toString());
        
    }//GEN-LAST:event_btnMostrarMouseClicked

    private void limparCampos() {
        txtNome.setText("");
        txtNatural.setText("");
        txtIdade.setText("");
        txtAltura.setText("");
        txtPeso.setText("");
    }
    
