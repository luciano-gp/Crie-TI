    private Usuario preencherObjeto() {
        objeto = new Usuario();
        
        objeto.setLogin(txtLogin.getText());
        objeto.setSenha(txtSenha.getText());
        objeto.setNome(txtNome.getText());
        
        return objeto;
    }
    
    private boolean validarCampos() {
        if (txtNome.getText().equals("")) {
            Globais.exibirMensagem("Preencher Nome");
            return false;
        } else if (txtLogin.getText().equals("")) {
            Globais.exibirMensagem("Preencher Login");
            return false;
        } else if (txtNome.getText().equals("")) {
            Globais.exibirMensagem("Preencher Senha");
            return false;
        }
        if (!txtSenha.getText().equals(txtSenha.getText())) {
            Globais.exibirMensagem("Senhas não conferem");
            return false;
        }
        return true;
    }

    private void limparCampos() {
        txtId.setText("");
        txtNome.setText("");
        txtLogin.setText("");
        txtSenha.setText("");
        txtSenha2.setText("");
    }
