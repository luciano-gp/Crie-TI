    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        try {
            if (validarCampos()) {
                controlador = new ControladorUsuario();
                boolean retorno = controlador.incluir(preencherObjeto());
                if (retorno) {
                    Globais.exibirMensagem("Usuário cadastrado com sucesso!");
                    limparCampos();
                } else {
                    Globais.exibirMensagem("Erro ao cadastrar usuário!");
                }
            }
        } catch (Exception ex) {

        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void txtIdKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtIdKeyPressed
        try {
            if(evt.getKeyCode() == KeyEvent.VK_ENTER) {
                controlador = new ControladorUsuario();
                Usuario objeto = controlador.buscar(Integer.parseInt(txtId.getText()));
                
                if (objeto != null) {
                    txtId.setEnabled(false);
                    txtLogin.setText(objeto.getLogin());
                    txtNome.setText(objeto.getNome());
                    txtSenha.setEnabled(false);
                    txtSenha2.setEnabled(false);
                }
            }
        } catch(Exception ex) {
            System.out.println("ERRO: " + ex.getMessage());
        }
    }//GEN-LAST:event_txtIdKeyPressed

    private void btnLimparActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparActionPerformed
        try {
            limparCampos();
            txtId.setEnabled(true);
            txtSenha.setEnabled(true);
            txtSenha2.setEnabled(true);
        } catch (Exception ex) {
            System.out.println("ERRO: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnLimparActionPerformed

